    private void sair(){
        int sai = JOptionPane.showConfirmDialog(null, "Deseja fechar a tela de venda rapida?", "Sair", JOptionPane.YES_NO_OPTION);
        if (sai == JOptionPane.YES_OPTION) {
            this.dispose();
        }
    }
    
    private void vender() throws ClassNotFoundException{
        entrada.setNaturezaEntrada("Venda " + txtNatVenda.getText());
        entrada.setDataEntrada(fd.dataSQL());
        double valorVenda = Double.parseDouble(JOptionPane.showInputDialog(null, "Qual o valor da venda?", "Valor da Venda"));
        entrada.setValorEntrada(valorVenda);
        fe.salvaEntrada(entrada);
        JOptionPane.showMessageDialog(null, "Venda Registrada com sucesso.", "Conclus√£o", JOptionPane.INFORMATION_MESSAGE);
    }


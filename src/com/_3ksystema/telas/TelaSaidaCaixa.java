    private void sairTelaSaida() {
        int sair = JOptionPane.showConfirmDialog(null, "Deseja Sair da tela de Saida de caixa?", "Aviso", JOptionPane.YES_NO_OPTION);
        if (sair == JOptionPane.YES_OPTION) {
            this.dispose();
        }
    }

    private void salvaSaida() throws ClassNotFoundException {
        saida.setNaturezaSaida(txtMotivo.getText());
        saida.setDataSaida(fd.formataData(txtData.getText()));
        saida.setValorSaida(Double.parseDouble(fn.ConverterFloat(txtValor.getText())));
        fs.salvaSaida(saida);
        limpaCampos();
    }

    private void editaSaida() throws ClassNotFoundException {
        fs.alteraSaida(saida);
    }

    private void excluiSaida() throws ClassNotFoundException {
        saida = fs.pesquisaSaida(txtMotivo.getText());
        fs.excluirSaida(idSaida);
        btnExclui.setEnabled(false);
        btnSalva.setEnabled(true);
        limpaCampos();
    }

    private Saidas pesquisaSaida() throws ClassNotFoundException {
        return fs.pesquisaSaida(txtMotivo.getText());
    }

    private void setaCampos() throws ClassNotFoundException {
        saida = pesquisaSaida();
        idSaida = saida.getIdSaida();
        txtMotivo.setText(saida.getNaturezaSaida());
        txtData.setText(fd.dataBr(saida.getDataSaida()));
        txtValor.setText(fn.FloatConvertido(Double.toString(saida.getValorSaida())));
    }

    private void limpaCampos() {
        txtMotivo.setText("");
        txtData.setText("");
        txtValor.setText("");
    }


    private void btnPesquisaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisaActionPerformed
        // TODO add your handling code here:
        if (txtMotivo.getText().isEmpty()) {
            try {
                ResultSet rs = fs.resgataSaidas(txtData.getText());
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(TelaSaidaCaixa.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else if (txtData.getText().isEmpty()) {
            try {
                saida = fs.pesquisaSaida(txtMotivo.getText());
                setaCampos();
                btnExclui.setEnabled(true);
                btnSalva.setText("Atualizar");
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(TelaSaidaCaixa.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnPesquisaActionPerformed


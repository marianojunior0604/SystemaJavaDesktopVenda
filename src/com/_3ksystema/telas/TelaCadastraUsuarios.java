    private void cadastraUsuario() throws NoSuchAlgorithmException{
        usuario.setNome_Usuario(txtNome.getText());
        usuario.setLogin_Usuario(txtLogin.getText());
        String senha = Hash.toHexadecimal(txtSenha.toString().getBytes());
        senha = Hash.toMD5Hash(senha);
        usuario.setSenha_Usuario(senha);
        usuario.setPerfil_Usuario(jcbPerfil.getSelectedItem().toString());
        fu.cadastraUsuario(usuario);
    }

    private void atualizaUsuario() throws NoSuchAlgorithmException, ClassNotFoundException{
        usuario.setNome_Usuario(txtNome.getText());
        usuario.setLogin_Usuario(txtLogin.getText());
        String senha = Hash.toHexadecimal(txtSenha.toString().getBytes());
        senha = Hash.toMD5Hash(senha);
        usuario.setSenha_Usuario(senha);
        usuario.setPerfil_Usuario(jcbPerfil.getSelectedItem().toString());
        fu.atualizaUsuario(usuario);
    }
    
    private Usuario pesquisaUsuario() throws ClassNotFoundException{
        return fu.pesquisaUsuario(txtNome.getText());
    }
    
    private void excluirUsuario(Usuario usuario) throws ClassNotFoundException{
        fu.excluiUsuario(usuario.getId_Usuario());
    }
    
    private void setaCampos(Usuario usuario){
        txtNome.setText(usuario.getNome_Usuario());
        txtLogin.setText(usuario.getLogin_Usuario());
        txtSenha.setText(usuario.getSenha_Usuario());
    }
    
    private void limpaCampos(){
        txtNome.setText("");
        txtLogin.setText("");
        txtSenha.setText("");
    }
    
    private void sair(){
        int sair = JOptionPane.showConfirmDialog(null, "Deseja sair da tela de Cadastro de Usuarios?", "Aviso", JOptionPane.YES_NO_OPTION);
        if (sair == JOptionPane.YES_OPTION) {
            this.dispose();
        }
    }

    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        // TODO add your handling code here:
        rvps = fv.listaVendasPeriodo(txtDataInicio.getText(), txtDataFim.getText());
        double valorTotal = 0.0;
        valorTotal = rvps.stream().map((rvp1) -> rvp1.getValorVenda()).reduce(valorTotal, (accumulator, _item) -> accumulator + _item);
        int totalVenda = rvps.size();
        lblTotaldeVendas.setText(String.valueOf(totalVenda));
        lblTotalVendido.setText("R$: " + String.valueOf(df.format(valorTotal)));
        listarEntradas(rvps);
    }//GEN-LAST:event_btnPesquisarActionPerformed

